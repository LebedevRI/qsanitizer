#include <QFileDialog>
#include <QFile>
#include <QMessageBox>
#include <QTextStream>

#include "mainwindow.h"
#include "ui_mainwindow.h"

MainWindow::MainWindow(QWidget *parent)
    : QMainWindow(parent), ui(new Ui::MainWindow)
{
    ui->setupUi(this);
}

MainWindow::~MainWindow() { delete ui; }

void MainWindow::on_action_Open_log_triggered()
{
    QString fileName
        = QFileDialog::getOpenFileName(this, tr("Open Log"), QString(),
                                       tr("Text Files (*.txt);;All Files (*)"));

    if (!fileName.isEmpty()) {
        QFile file(fileName);
        if (!file.open(QIODevice::ReadOnly)) {
            QMessageBox::critical(this, tr("Error"), tr("Could not open file"));
            return;
        }
        QTextStream in(&file);
        QString longstring(in.readAll());
        QStringList strings = longstring.split("\n\n");
        ui->listWidget->insertItems(0, strings);
        file.close();
    }
}
